		public static void suma(Integer a, Integer b){
			System.out.println("\n#####SUMA##### ");
			System.out.println(a+"+"+b+"="+(a+b));
			
		}
		public static void rest(Integer a, Integer b){
			System.out.println("\n#####RESTA##### ");
			System.out.println(a+"-"+b+"="+(a-b));
			
		}
